/**
 * Admin Dashboard Cards Constants
 * Defines the indicator cards data for the admin dashboard
 */

import { TabData } from '@app-types/components';
import type { DashboardGraphBlock } from '@app-types/dashboardGraphs';

export interface DashboardCard {
  id: string;
  icon?: string;
  iconColor?: string;
  title: string; // Translation key
  description: string; // Translation key
  navigationUrl?: string;
  subCards?: DashboardCard[]; // Sub-cards for hierarchical navigation
  status?: {
    type: string;
    label: string; // Translation key
  };
}

export interface MetricCardData {
  id: string;
  title: string; // Translation key
  value: string | number; // Display value (count or percentage) - shown directly
  subtitle: string; // Translation key
  count?: string; // Optional count to show before subtitle
  color?: string; // Optional color for the main value
}

export interface CardViewSection {
  title: string; // Translation key
  metricCards: MetricCardData[];
}

export interface CardViewBreakdownItem {
  id: string;
  label: string; // Translation key
  color: string;
  count: string | number;
  percentage: string; // e.g. "38%"
}

export interface CardViewBreakdownSection {
  title: string; // Translation key
  items: CardViewBreakdownItem[];
}

export interface CardViewData {
  tabs: TabData[];
  metricCards: MetricCardData[];
  sections?: CardViewSection[];
  breakdownSections?: CardViewBreakdownSection[];
  graphsBlocks?: DashboardGraphBlock[];
  graphsPlaceholderKey?: string;
  insightsTitle?: string; // Translation key
  insightsItems?: string[]; // Translation keys
}

